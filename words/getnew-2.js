// Existing words were copied over from words.js into this script
// The manually reviewed version of this round's output is new2-reviewed.txt
// That is used directly by the custom build task

const fs = require("fs");

const prevWords = new Set();
for (const word of puzzleWords())
  prevWords.add(shiftStr(word, -1));
for (const word of otherWords())
  prevWords.add(shiftStr(word, -1));

const newWordSet = new Set();
addNewWords("5betu-filt.txt");
addNewWords("k5-filt.txt");
function addNewWords(fn) {
  let newWordsStr = fs.readFileSync(fn, "utf8");
  let newWordsArr = newWordsStr.split(/\r?\n/);
  for (const word of newWordsArr) {
    if (prevWords.has(word)) continue;
    newWordSet.add(word);
  }
}

let newWordsArr = [...newWordSet];
let out = "";
for (const word of newWordsArr) {
  if (prevWords.has(word)) continue;
  out += word + "\n";
}
fs.writeFileSync("new2.txt", out);

function shiftStr(str, val) {
  let res = "";
  for (let i = 0; i < str.length; ++i) {
    let cp = str.charCodeAt(i);
    res += String.fromCodePoint(cp + val);
  }
  return res;
}

